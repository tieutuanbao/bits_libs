Color_RGB_t ColorRGB_fromHexCodeString(char *hexString) {
	Color_RGB_t colorTemp = {0, 0, 0};
	if(hexString[0] == '#') {
		hexString++;
	}
	*((long *)&colorTemp) = Bits_String_ToInt(hexString, 6, 16);
	return (Color_RGB_t){colorTemp.B, colorTemp.G, colorTemp.R};
}